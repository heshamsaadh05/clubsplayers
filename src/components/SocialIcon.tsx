interface SocialIconProps {
  platformId: string;
  iconUrl?: string;
  className?: string;
}

export const SocialIcon = ({ platformId, iconUrl, className = "w-5 h-5" }: SocialIconProps) => {
  // If custom icon URL is provided
  if (iconUrl) {
    return <img src={iconUrl} alt={platformId} className={className} />;
  }

  // Built-in icons
  const iconMap: Record<string, React.FC<{ className?: string }>> = {
    facebook: Facebook,
    twitter: Twitter,
    instagram: Instagram,
    youtube: Youtube,
    tiktok: TikTokIcon,
    whatsapp: MessageCircle,
    snapchat: SnapchatIcon,
    linkedin: Linkedin,
    telegram: Send,
    pinterest: PinterestIcon,
  };

  const IconComponent = iconMap[platformId];

  if (IconComponent) {
    return <IconComponent className={className} />;
  }

  // Default fallback - first letter
  return (
    <span className={`font-bold text-sm ${className}`}>
      {platformId.charAt(0).toUpperCase()}
    </span>
  );
};

